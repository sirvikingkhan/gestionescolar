<script>// Funcionalidad de arrastrar y soltar
const products = document.querySelectorAll('.product');
const categories = document.querySelectorAll('.category');
const errorModal = document.getElementById('errorModal');
const closeBtn = document.querySelector('.close-btn');

// Diccionario que mapea productos a sus categorías correctas
const correctCategories = {
    'macas': 'frescos',
    'bananas': 'frescos',
    'morangos': 'frescos',
    'abacates': 'frescos',
    'pimentoes': 'frescos',
    'pao': 'graos',
    'arroz': 'graos',
    'macarrao': 'graos',
    'milho': 'graos',
    'peito_frango': 'proteinas',
    'ovos': 'proteinas',
    'carne_moida': 'proteinas',
    'peito_peru': 'proteinas',
    'manteiga': 'laticinios',
    'queijo_fatiado': 'laticinios',
    'queijo_ralado': 'laticinios',
    'leite': 'laticinios',
    'cafe': 'bebidas',
    'cha': 'bebidas',
    'refrigerante': 'bebidas',
    'suco': 'bebidas'
};

// Evento para iniciar el arrastre
products.forEach(product => {
    product.addEventListener('dragstart', (e) => {
        e.dataTransfer.setData('text', e.target.id);
    });
});

// Permitir que los elementos sean soltados en las categorías
categories.forEach(category => {
    category.addEventListener('dragover', (e) => {
        e.preventDefault();
    });

    // Evento para soltar el producto
    category.addEventListener('drop', (e) => {
        const productId = e.dataTransfer.getData('text');
        const product = document.getElementById(productId);
        const correctCategory = correctCategories[productId];

        // Verificar si la categoría correcta coincide con la categoría actual
        if (category.id === correctCategory) {
            category.appendChild(product); // Producto colocado en la categoría correcta
        } else {
            // Mostrar el modal de error
            errorModal.style.display = 'block';
        }
    });
});

// Cerrar el modal
closeBtn.addEventListener('click', () => {
    errorModal.style.display = 'none';
});

// Cerrar el modal si el usuario hace clic fuera de él
window.addEventListener('click', (e) => {
    if (e.target == errorModal) {
        errorModal.style.display = 'none';
    }
});

</script>


